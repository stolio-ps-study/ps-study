tetrominos = (((0, 1), (0, 2), (0, 3)), ((1, 0), (2, 0), (3, 0)), ((1, 0), (0, 1), (1, 1)),
              ((1, 0), (2, 0), (2, 1)), ((1, 0), (0, 1), (0, 2)), ((0, 1), (1, 1), (2, 1)), ((0, 1), (0, 2), (-1, 2)),
              ((1, 0), (2, 0), (2, -1)), ((1, 0), (0, -1), (0, -2)), ((0, -1), (1, -1), (2, -1)), ((0, -1), (0, -2), (-1, -2)),
              ((1, 0), (1, 1), (2, 1)), ((0, 1), (-1, 1), (-1, 2)), ((-1, 0), (-1, 1), (-2, 1)), ((0, 1), (1, 1), (1, 2)),
              ((0, 1), (0, 2), (1, 1)), ((1, 0), (1, -1), (2, 0)), ((0, 1), (-1, 1), (0, 2)), ((1, 0), (1, 1), (2, 0)))


n, m = map(int, input().split())
paper = [list(map(int, input().split())) for _ in range(n)]

ans = 0
for x in range(n):
    for y in range(m):
        for t in tetrominos:
            num = paper[x][y]
            for dx, dy in t:
                nx, ny = x+dx, y+dy
                if 0 <= nx < n and 0 <= ny < m:
                    num += paper[nx][ny]
                else:
                    num = 0
                    break
            ans = max(num, ans)
print(ans)